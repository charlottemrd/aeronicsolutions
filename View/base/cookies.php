<h1> Les cookies</h1>
<p >
Les cookies sont de petits fichiers qui stockent des informations sur votre ordinateur, téléviseur, téléphone mobile ou autre appareil. </br>
L’organisme qui place un cookie sur votre appareil peut ainsi vous reconnaître, à travers les différents sites internet, services et appareils que vous utilisez ou visitez, ainsi que d’une session de navigation à l’autre. Les cookies sont très utiles. </br>
</p>


<div class="Cookdonnes">
	<h2> A quoi ils servent  ?</h2>
	<p>
		Pour des besoins de statistiques et d'affichage, le présent site utilise des cookies.</br>
		Par exemple :
		<ul>
			<li> Ils peuvent mémoriser vos identifiants de connexion afin que vous n’ayez pas à les saisir à chaque fois que vous vous connectez à un service.</li>
			<li> Ils nous indiquent les contenus ou services qui sont les plus populaires sur nos sites car ils nous permettent de savoir (directement ou par l’intermédiaire d’un tiers) quelles sont les pages les plus visitées, les fonctionnalités les plus utilisées et le temps passé sur ces pages. En analysant ce type d’informations, nous sommes mieux à même d’adapter les contenus ou services de notre site internet et de vous offrir une meilleure expérience utilisateur.</li>
			<li> Ils nous aident, nous ainsi que les tiers concernés, à déterminer les publicités que vous avez vues, afin de vous proposer une communication et un service plus adapté à vos attentes.</li>
			<li> Ils collectent des informations relatives à votre utilisation de nos services, ainsi que des données relatives à votre navigation sur d’autres sites et applications mobiles ce qui nous permet ainsi qu’aux tiers concernés, de vous proposer du contenu et des publicités pertinentes.</li>
		</ul>
	</p>

	<h2>La gestion des cookies </h2>
	<p>
		 Vous pouvez modifier à tout moment vos préférences en matière de cookies dans notre Outil de gestion des cookies</br>
		 Vous pouvez également supprimer ou bloquer à tout moment les cookies déposés sur votre appareil, en mettant à jour les paramètres de votre navigateur</br>
		 Veuillez noter que la désactivation de certains cookies peut affecter la réalisation de certaines transactions sur le site internet ou bien dégrader votre expérience utilisateur et/ou nous empêcher de vous reconnaître votre navigateur d'une visite à l'autre.

		Nous vous remercions de votre visite et nous vous souhaitons une bonne navigation sur notre site !
	</p>
	
